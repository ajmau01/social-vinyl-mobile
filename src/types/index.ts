/**
 * WebSocket Protocol Types
 */

export type WebSocketMessageType =
    | 'WELCOME' | 'welcome'
    | 'ACCESS_LEVEL' | 'access-level'
    | 'SESSION_JOINED' | 'session-joined'
    | 'NOW_PLAYING' | 'now-playing'
    | 'now-playing-cleared'
    | 'SESSION_ENDED' | 'session-ended'
    | 'ERROR' | 'error'
    | 'admin-login-success'
    // New types for Issue #125
    | 'PROTOCOL_HANDSHAKE'
    | 'PROTOCOL_ACK'
    | 'CLIENT_ACTION'
    | 'ACTION_ACK'
    // Session Management (Issue #128)
    | 'session-created'
    | 'session-left'
    | 'session-list'
    // Issue #126
    | 'BIN_STATE' | 'bin-state'
    | 'STATE' | 'state';

export interface WebSocketMessage {
    type?: WebSocketMessageType;
    messageType?: WebSocketMessageType;
    payload?: LoginResult | SyncResult | NowPlaying | { message: string } | Record<string, unknown>;
    sessionId?: string;
    authToken?: string;
    sessionSecret?: string;
    joinCode?: string;
    name?: string;
    publicUrl?: string;
    hostUsername?: string;
    isPermanent?: boolean;
    sessions?: SessionCard[];
    username?: string;
    startedAt?: number;
    // Issue #125: Add actionId and status for ACK correlation
    actionId?: string;
    status?: 'success' | 'error';
    album?: {
        title: string;
        artist: string;
        releaseId: number;
        instanceId?: number;
        coverImage: string;
        tracks?: Track[];
    };
    // Issue #126: Bin State Payload
    bin?: BinItem[];
    message?: string;
}

export interface ProtocolHandshakeMessage {
    type: 'PROTOCOL_HANDSHAKE';
    version: string;
    capabilities: string[];
}

export interface ProtocolAckMessage {
    type: 'PROTOCOL_ACK';
    supported: boolean;
    enabledFeatures: string[];
}

export interface ClientActionMessage {
    type: 'CLIENT_ACTION';
    action: string;
    actionId: string;
    payload: any;
}

export interface ActionAckMessage {
    type: 'ACTION_ACK';
    actionId: string;
    status: 'success' | 'error';
    error?: string;
    data?: any;
}


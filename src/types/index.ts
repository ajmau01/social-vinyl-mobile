/**
 * Domain types for Social Vinyl Mobile
 */

/**
 * Result Pattern (Critical for Phase 1)
 */
export type Result<T, E = Error> =
    | { success: true; data: T }
    | { success: false; error: E };

export type AsyncResult<T, E = Error> = Promise<Result<T, E>>;

export interface WebSocketCallbacks {
    onConnectionStateChange(state: ConnectionState): void;
    onMessage(message: WebSocketMessage): void;
    onError(error: Error): void;
    // Semantic Callbacks for architectural cleanup
    onSessionJoined?(data: { sessionId: string; authToken?: string; username?: string }): void;
    onNowPlaying?(data: NowPlaying): void;
    onSessionEnded?(): void;
    onAccessLevel?(level: string): void;
    // Issue #126
    onBinState?(items: BinItem[]): void;
}

export interface SyncCallbacks {
    onProgress(progress: number): void;
    onStatusChange(status: SyncStatus): void;
}

export interface Release {
    id: number;
    releaseId?: number; // Backend alias
    instanceId?: number; // UNIQUE identifier from Discogs (or instance PK from backend)
    userId: string; // SCOPING: User ID for isolation
    title: string;
    artist: string;
    thumb_url: string | null;
    coverImage?: string; // Backend payload
    added_at: number;
    year?: string;
    genres?: string;
    label?: string;
    format?: string;
    tracks?: string; // JSON String
    isSaved?: boolean;
    isNotable?: boolean;
    spinCount?: number;
    playedAt?: number; // History timestamp (ms)
    totalDuration?: number; // Total duration in seconds (Added for Progress Ring)
}

/**
 * NowPlaying Interface
 * Aligned with specification in Issue #23.
 */
export interface NowPlaying {
    track: string;
    artist: string;
    album: string;
    albumArt?: string;
    timestamp?: number;
    releaseId?: string; // Added for internal linking
    duration?: number;   // Total duration in ms
    position?: number;   // Current playback position in ms
    userHasLiked?: boolean; // If current user has liked this track
    playedBy?: string;   // Attribution - who added this to the bin
    likeCount?: number;  // Total thumbs up for this track
    playedAt?: number;   // Timestamp when playback started (ms)
}

export interface BinItem extends Release {
    /**
     * Identity lifecycle: `item.id` is the Discogs releaseId BEFORE `confirmAdd`,
     * and becomes `instanceId` (backend DB row PK) AFTER `confirmAdd` or `bin-state` sync.
     * Always use `item.id` as the store lookup key. Use `item.releaseId` for Discogs API calls.
     * See: ListeningBinSyncService.removeAlbum JSDoc.
     */
    addedTimestamp: number;
    // Issue #126: Sync status for optimistic updates
    status?: 'synced' | 'pending' | 'error';
    // Issue #126: Helper to track temporary ID for optimistic adds
    tempId?: string;
    // Issue #126: Unique ID generated by frontend to handle duplicate album entries
    frontendId?: string;
    // Backend sends 'requestedBy' instead of 'userId' for server-synced items
    requestedBy?: string;
    // Backend-assigned UUID used for ownership verification on remove
    clientUUID?: string;
}

export type SyncStatus = 'idle' | 'syncing' | 'complete' | 'error';

export type ConnectionState = 'disconnected' | 'connecting' | 'connected' | 'reconnecting';

